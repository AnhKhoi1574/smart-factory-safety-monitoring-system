"""Dataset splitting helpers for train/validation/test generation."""

from __future__ import annotations

import random
import shutil
from pathlib import Path
from typing import Any


VALID_IMAGE_EXTENSIONS = {".jpg", ".jpeg", ".png"}
SPLIT_NAMES = ("train", "val", "test")


def split_dataset(
    master_images_dir: Path,
    master_labels_dir: Path,
    output_dir: Path,
    train_ratio: float,
    val_ratio: float,
    test_ratio: float,
    seed: int = 42,
    use_external_test_set: bool = True,
    overwrite: bool = False,
) -> dict[str, Any]:
    """Split cleaned original image-label pairs into YOLO split folders.

    Args:
        master_images_dir: Directory containing merged original images.
        master_labels_dir: Directory containing merged original YOLO labels.
        output_dir: Directory where ``train``, ``val``, and ``test`` folders live.
        train_ratio: Fraction of master originals assigned to training.
        val_ratio: Fraction of master originals assigned to validation.
        test_ratio: Fraction assigned to test when no external test set is used.
        seed: Seed used for deterministic shuffling.
        use_external_test_set: If ``True``, leave ``output_dir/test`` unchanged
            and require ``test_ratio == 0``.
        overwrite: If ``True``, clear generated split folders before copying.

    Returns:
        A summary dictionary with counts and notes for each split.

    Raises:
        FileNotFoundError: If master image or label folders are missing.
        ValueError: If ratios are invalid or master image-label pairs mismatch.
        FileExistsError: If generated split folders already contain files and
            ``overwrite`` is ``False``.
    """
    master_images_dir = Path(master_images_dir)
    master_labels_dir = Path(master_labels_dir)
    output_dir = Path(output_dir)

    _validate_ratios(
        train_ratio=train_ratio,
        val_ratio=val_ratio,
        test_ratio=test_ratio,
        use_external_test_set=use_external_test_set,
    )
    pairs = find_image_label_pairs(master_images_dir, master_labels_dir)
    for split_name in SPLIT_NAMES:
        _ensure_split_dirs(output_dir, split_name)

    generated_splits = ("train", "val") if use_external_test_set else SPLIT_NAMES
    _prepare_output_folders(output_dir, generated_splits, overwrite=overwrite)

    shuffled_pairs = pairs.copy()
    random.Random(seed).shuffle(shuffled_pairs)
    split_pairs = _partition_pairs(
        pairs=shuffled_pairs,
        train_ratio=train_ratio,
        val_ratio=val_ratio,
        test_ratio=test_ratio,
        use_external_test_set=use_external_test_set,
    )

    for split_name in generated_splits:
        copy_pairs_to_split(split_pairs[split_name], output_dir, split_name)

    summary = summarize_split(output_dir)
    summary["train"].update(
        {
            "source": "master_original",
            "notes": "generated by notebook 03",
        }
    )
    summary["val"].update(
        {
            "source": "master_original",
            "notes": "generated by notebook 03",
        }
    )

    if use_external_test_set:
        test_count = summary["test"]["num_images"]
        notes = "preserved from notebook 01"
        if test_count == 0:
            notes = (
                "external test set configured but no test files were found; "
                "train/val split still generated"
            )
        summary["test"].update(
            {
                "source": "external_test_set",
                "notes": notes,
            }
        )
    else:
        summary["test"].update(
            {
                "source": "master_original",
                "notes": "generated by notebook 03",
            }
        )

    return summary


def find_image_label_pairs(
    images_dir: Path,
    labels_dir: Path,
) -> list[tuple[Path, Path]]:
    """Return sorted image-label pairs from matching YOLO folders."""
    images_dir = Path(images_dir)
    labels_dir = Path(labels_dir)

    if not images_dir.exists():
        raise FileNotFoundError(f"Master images directory not found: {images_dir}")
    if not labels_dir.exists():
        raise FileNotFoundError(f"Master labels directory not found: {labels_dir}")

    image_paths_by_stem: dict[str, list[Path]] = {}
    for image_path in sorted(images_dir.iterdir()):
        if image_path.is_file() and image_path.suffix.lower() in VALID_IMAGE_EXTENSIONS:
            image_paths_by_stem.setdefault(image_path.stem, []).append(image_path)

    label_paths_by_stem = {
        label_path.stem: label_path
        for label_path in sorted(labels_dir.iterdir())
        if label_path.is_file() and label_path.suffix.lower() == ".txt"
    }

    duplicate_stems = sorted(
        stem for stem, paths in image_paths_by_stem.items() if len(paths) > 1
    )
    if duplicate_stems:
        raise ValueError(
            "Duplicate image stems found in master images: "
            f"{', '.join(duplicate_stems[:10])}"
        )

    image_stems = set(image_paths_by_stem)
    label_stems = set(label_paths_by_stem)
    missing_labels = sorted(image_stems - label_stems)
    missing_images = sorted(label_stems - image_stems)
    if missing_labels or missing_images:
        messages: list[str] = []
        if missing_labels:
            messages.append(
                "images without labels: " + ", ".join(missing_labels[:10])
            )
        if missing_images:
            messages.append(
                "labels without images: " + ", ".join(missing_images[:10])
            )
        raise ValueError("Master image-label mismatch; " + "; ".join(messages))

    if not image_stems:
        raise ValueError(f"No image-label pairs found in {images_dir}")

    return [
        (image_paths_by_stem[stem][0], label_paths_by_stem[stem])
        for stem in sorted(image_stems)
    ]


def clear_split_folder(output_dir: Path, split_name: str) -> None:
    """Remove files from one split's image and label folders."""
    for child_dir in _split_dirs(Path(output_dir), split_name):
        if not child_dir.exists():
            continue
        for file_path in child_dir.iterdir():
            if file_path.is_file():
                file_path.unlink()


def copy_pairs_to_split(
    pairs: list[tuple[Path, Path]],
    output_dir: Path,
    split_name: str,
) -> None:
    """Copy image-label pairs into one split folder, preserving filenames."""
    images_dir = Path(output_dir) / split_name / "images"
    labels_dir = Path(output_dir) / split_name / "labels"
    images_dir.mkdir(parents=True, exist_ok=True)
    labels_dir.mkdir(parents=True, exist_ok=True)

    for image_path, label_path in pairs:
        shutil.copy2(image_path, images_dir / image_path.name)
        shutil.copy2(label_path, labels_dir / label_path.name)


def summarize_split(output_dir: Path) -> dict[str, dict[str, Any]]:
    """Count image and label files in each split folder."""
    summary: dict[str, dict[str, Any]] = {}
    for split_name in SPLIT_NAMES:
        images_dir = Path(output_dir) / split_name / "images"
        labels_dir = Path(output_dir) / split_name / "labels"
        num_images = _count_files(images_dir, VALID_IMAGE_EXTENSIONS)
        num_labels = _count_files(labels_dir, {".txt"})
        summary[split_name] = {
            "split": split_name,
            "num_images": num_images,
            "num_labels": num_labels,
            "source": "",
            "notes": "",
        }
    return summary


def _validate_ratios(
    train_ratio: float,
    val_ratio: float,
    test_ratio: float,
    use_external_test_set: bool,
) -> None:
    ratios = {
        "train": train_ratio,
        "val": val_ratio,
        "test": test_ratio,
    }
    negative_ratios = [name for name, ratio in ratios.items() if ratio < 0]
    if negative_ratios:
        raise ValueError(f"Split ratios must be non-negative: {negative_ratios}")

    if use_external_test_set:
        if abs(test_ratio) > 1e-9:
            raise ValueError(
                "use_external_test_set=True requires split.test to be 0.00"
            )
        total = train_ratio + val_ratio
        if abs(total - 1.0) > 1e-6:
            raise ValueError(
                "With external test enabled, split.train + split.val must equal 1.0"
            )
    else:
        total = train_ratio + val_ratio + test_ratio
        if abs(total - 1.0) > 1e-6:
            raise ValueError(
                "Without external test, split.train + split.val + split.test "
                "must equal 1.0"
            )


def _prepare_output_folders(
    output_dir: Path,
    split_names: tuple[str, ...],
    overwrite: bool,
) -> None:
    occupied_splits = [
        split_name
        for split_name in split_names
        if _split_has_files(output_dir, split_name)
    ]
    if occupied_splits and not overwrite:
        raise FileExistsError(
            "Split folder(s) already contain files: "
            f"{', '.join(occupied_splits)}. "
            "Set overwrite=True to regenerate generated splits."
        )

    if overwrite:
        for split_name in split_names:
            clear_split_folder(output_dir, split_name)


def _partition_pairs(
    pairs: list[tuple[Path, Path]],
    train_ratio: float,
    val_ratio: float,
    test_ratio: float,
    use_external_test_set: bool,
) -> dict[str, list[tuple[Path, Path]]]:
    if use_external_test_set:
        train_count, val_count, _ = _split_counts(
            total=len(pairs),
            ratios=(train_ratio, val_ratio, 0.0),
        )
        return {
            "train": pairs[:train_count],
            "val": pairs[train_count : train_count + val_count],
            "test": [],
        }

    train_count, val_count, test_count = _split_counts(
        total=len(pairs),
        ratios=(train_ratio, val_ratio, test_ratio),
    )
    train_end = train_count
    val_end = train_end + val_count
    return {
        "train": pairs[:train_end],
        "val": pairs[train_end:val_end],
        "test": pairs[val_end : val_end + test_count],
    }


def _split_counts(total: int, ratios: tuple[float, float, float]) -> tuple[int, int, int]:
    raw_counts = [total * ratio for ratio in ratios]
    counts = [int(raw_count) for raw_count in raw_counts]
    remainder = total - sum(counts)
    fractional_order = sorted(
        range(len(raw_counts)),
        key=lambda index: raw_counts[index] - counts[index],
        reverse=True,
    )
    for index in fractional_order[:remainder]:
        counts[index] += 1
    return counts[0], counts[1], counts[2]


def _ensure_split_dirs(output_dir: Path, split_name: str) -> None:
    for child_dir in _split_dirs(output_dir, split_name):
        child_dir.mkdir(parents=True, exist_ok=True)


def _split_dirs(output_dir: Path, split_name: str) -> tuple[Path, Path]:
    split_dir = Path(output_dir) / split_name
    return split_dir / "images", split_dir / "labels"


def _split_has_files(output_dir: Path, split_name: str) -> bool:
    return any(
        file_path.is_file()
        for child_dir in _split_dirs(output_dir, split_name)
        if child_dir.exists()
        for file_path in child_dir.iterdir()
    )


def _count_files(directory: Path, suffixes: set[str]) -> int:
    if not directory.exists():
        return 0
    return sum(
        1
        for path in directory.iterdir()
        if path.is_file() and path.suffix.lower() in suffixes
    )
